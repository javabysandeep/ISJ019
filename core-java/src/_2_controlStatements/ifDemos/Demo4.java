package _2_controlStatements.ifDemos;

public class Demo4 {
    public static void main(String[] args) {
        //to check if the number is even or odd
        int number = 10;

        if (number % 2 == 0) {
            System.out.println(number + " is an even number");
        } else {
            System.out.println(number + " is an odd number");
        }

    }
}
