package _0_introduction;

public class DataTypeDemo5 {

    public static void main(String[] args) {

        // store a number using byte data type

        byte byteValue = 127;

        System.out.println("byte value from the variable : " + byteValue);


        // store a number using short data type

        short shortValue = 32767;

        System.out.println("short value from the variable : " + shortValue);


        // store a number using int data type

        int intValue = 2147483647;

        System.out.println("int value from the variable : " + intValue);


        // store a number using long data type

        long longValue = 2147483648L;

        System.out.println("long value from the variable : " + longValue);


        // store a number using float data type

        float floatValue = 99.33F;

        System.out.println("float value from the variable : " + floatValue);


        // store a number using double data type

        double doubleValue = 99999999.33D;

        System.out.println("double value from the variable : " + doubleValue);


        // store a character using char data type

        char charValue = 'A';
        char charValue2 = 66;

        System.out.println("character value from the variable : " + charValue);
        System.out.println("character value from the variable : " + charValue2);


        // store boolean value usign boolean data type

        boolean booleanValue = true;
        System.out.println("boolean value : " + booleanValue);


    }

}